class RaceTrack(Environment):
    def __init__(self, track, name="Racetrack"):
        self.track = track
        self.max_speed = 5
        vels = 2 * self.max_speed + 1
        self.vel_shift = vels // 2
        self.state_space = (
            # x position
            self.track.shape[0],
            # y position
            self.track.shape[1],
            # x speed
            vels,
            # y speed
            vels,
        )
        self.action_space = (3, 3)
        self.test = False
        super().__init__(
            np.prod(self.state_space),
            np.prod(self.action_space),
            name,
        )
        self.reset()

    def reset(self):
        start_xs, start_ys = np.where(self.track == 2)
        i = np.random.choice(range(len(start_ys)))
        # (x, y, x_vel, y_vel)
        self.state = (start_xs[i], start_ys[i], 0, 0)
        return self.state_to_index(self.state)
        
    def index_to_action(i):
        actions = np.array([
            (-1, -1), (-1, 0), (-1, 1),
            ( 0, -1), ( 0, 0), ( 0, 1),
            ( 1, -1), ( 1, 0), ( 1, 1),
        ])
    
        return actions[i]

    def clamp(val, lower, upper):
        return max(lower, min(upper, val))

    def clamp_speed(self, speed):
        return RaceTrack.clamp(speed, -self.max_speed, self.max_speed)

    def set_train(self):
        self.test = False

    def set_test(self):
        self.test = True

    def step(self, action_index):
        state, reward, done = self._step(action_index)
        return self.state_to_index(state), reward, done

    def _step(self, action_index):
        x, y, x_vel_old, y_vel_old = self.state
        dx, dy = RaceTrack.index_to_action(action_index)
        x_vel = self.clamp_speed(x_vel_old + dx)
        y_vel = self.clamp_speed(y_vel_old + dy)

        # Reset speed in one direction if both are zero
        if x_vel == 0 and y_vel == 0:
            if np.random.random() > 0.5:
                x_vel = x_vel_old
            else:
                y_vel = y_vel_old

        # During training, inject some noise by ignoring actions
        # sometimes
        if not self.test and np.random.random() < 0.1:
            x_vel, y_vel = x_vel_old, y_vel_old

        to_go = [x_vel, y_vel]
        while to_go[0] != 0 or to_go[1] != 0:
            if to_go[0] != 0 and to_go[1] != 0:
                d = 0 if np.random.random() > 0.5 else 1
            elif to_go[0] != 0:
                d = 0
            else:
                d = 1
                
            sign = to_go[d] / abs(to_go[d])
            if d == 0:
                x += sign
            else:
                y += sign
            to_go[d] -= sign

            x, y = int(x), int(y)
            if x not in range(self.track.shape[0]) or y not in range(self.track.shape[1]):
                self.reset()
                return self.state, -2, False
            loc = self.track[x,y]
            # Hit a boundary, loss!
            if loc == 0:
                self.reset()
                return self.state, -2, False
            # Reached the finish line, win!
            elif loc == 3:
                self.state = (int(x), int(y), int(x_vel), int(y_vel))
                return self.state, 100, True

        self.state = (int(x), int(y), int(x_vel), int(y_vel))
        return self.state, -1, False

    def state_to_index(self, state):
        x, y, x_vel, y_vel = state
        x_vel += self.vel_shift
        y_vel += self.vel_shift
        return np.ravel_multi_index((x, y, x_vel, y_vel), self.state_space)

    def index_to_state(self, index):
        x, y, x_vel, y_vel = np.unravel_index(index, self.state_space)
        x_vel -= self.vel_shift
        y_vel -= self.vel_shift
        return x, y, x_vel, y_vel

    def state_repr(self, state):
        track_state = self.track.copy()
        track_state[state[0], state[1]] = 10
        return track_state

